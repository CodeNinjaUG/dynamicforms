<script setup>
import { reactive } from "vue";
const form = reactive([
  {
    firstname: "",
    lasttname: "",
    password: "",
  },
]);

const add = () => {
  form.push({
    firstname: "",
    lasttname: "",
    password: "",
  });
};

const sub = (index) => {
  if (index > 0) {
    form.splice(index, 1);
  }
};
</script>
<template>
  <div class="flex justify-center items-center gap-x-4 mt-10">
    <svg
      @click="add"
      xmlns="http://www.w3.org/2000/svg"
